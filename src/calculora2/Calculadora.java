    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (!modooscuro) {
            

            CambiarBtn2(btn_ultimo);
            CambiarBtn2(btn_suma);
            CambiarBtn2(btn_resta);
            CambiarBtn2(btn_multi);
            CambiarBtn2(btn_div);
            CambiarBtn2(btn_borrar);
            CambiarBtn2(btn_mod);

            CambiarBnt(btn_1);
            CambiarBnt(btn_2);
            CambiarBnt(btn_3);
            CambiarBnt(btn_4);
            CambiarBnt(btn_5);
            CambiarBnt(btn_6);
            CambiarBnt(btn_7);
            CambiarBnt(btn_8);
            CambiarBnt(btn_9);
            CambiarBnt(btn_0);
            CambiarBnt(btn_punto);
            
            jPanel1.setBackground(new Color(33, 43, 65));
            jPanel2.setBackground(new Color(46, 57, 81));
            txt_operaciones.setForeground(Color.decode("#0db387"));
            txt_resultado.setForeground(Color.decode("#0db387"));

            jButton2.setIcon(new ImageIcon(getClass().getResource("/images/darkmode_2.png")));
            //Estilo de boton =
            btn_igual.setIcon(new ImageIcon(getClass().getResource("/images/btn_igual.png")));
            btn_igual.setRolloverIcon(new ImageIcon(getClass().getResource("/images/btn_presedar.png")));
            modooscuro = true;

        } else {
            jPanel1.setBackground(Color.decode("#f4fdfb"));
            jPanel2.setBackground(Color.white);
            txt_operaciones.setForeground(Color.decode("#373e47"));
            txt_resultado.setForeground(Color.decode("#373e47"));
            
            jButton2.setIcon(new ImageIcon(getClass().getResource("/images/darkmode_1.png")));
            //Estilo de boton =
            btn_igual.setIcon(new ImageIcon(getClass().getResource("/images/btn3.png")));
            btn_igual.setRolloverIcon(new ImageIcon(getClass().getResource("/images/boton1_preseend.png")));

            CambiarModBlanco(btn_0);
            CambiarModBlanco(btn_1);
            CambiarModBlanco(btn_2);
            CambiarModBlanco(btn_3);
            CambiarModBlanco(btn_4);
            CambiarModBlanco(btn_5);
            CambiarModBlanco(btn_6);
            CambiarModBlanco(btn_7);
            CambiarModBlanco(btn_8);
            CambiarModBlanco(btn_9);
            CambiarModBlanco(btn_punto);

            CambiarModBlacodar(btn_ultimo);
            CambiarModBlacodar(btn_borrar);
            CambiarModBlacodar(btn_suma);
            CambiarModBlacodar(btn_resta);
            CambiarModBlacodar(btn_multi);
            CambiarModBlacodar(btn_div);
            CambiarModBlacodar(btn_mod);

            modooscuro = false;
        }


    }//GEN-LAST:event_jButton2ActionPerformed

